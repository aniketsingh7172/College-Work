from turtle import *
t=Turtle()
sc=Screen()
t.speed(-1)
t.pensize(3)
t.hideturtle()
sc.tracer(-1)
t.pu()
t.goto(375,375)
t.pd()
t.setheading(-180)
c=['green','blue','red','yellow']
for i in range(4):
    t.fillcolor(c[i%4])
    t.begin_fill()
    t.fd(300)
    t.lt(90)
    t.fd(300)
    t.lt(90)
    t.fd(300)
    t.lt(90)
    t.fd(300)
    t.bk(750)
    t.end_fill()

def s(x,y):
    t.pu()
    t.goto(x,y)
    t.pd()
    t.color('white')
    t.begin_fill()
    t.fd(170)
    t.lt(90)
    t.fd(170)
    t.lt(90)
    t.fd(170)
    t.lt(90)
    t.fd(170)
    t.end_fill()

def l(m,x,y):
    t.pu()
    t.goto(x,y)
    t.pd()
    for i in range(4):
        t.pu()
        t.fd(50)
        t.lt(90)
        t.fd(50)
        t.lt(90)
        t.fd(50)
        t.lt(90)
        t.fd(50)
        t.bk(25)
        t.pd()
        t.color(c[m])
        t.begin_fill()
        t.circle(25)
        t.end_fill()
        t.pu()
        t.fd(25)
        t.bk(125)
        t.pd()

def f(a,b,x,y):
    t.pu()
    t.goto(x,y)
    t.pd()
    # t.pencolor('black')
    t.color(c[b])
    t.begin_fill()
    t.setheading(a)
    t.fd(150)
    t.rt(-135)
    t.fd(106)
    t.lt(90)
    t.fd(106)
    t.end_fill()

def r(n,x,y):
    t.pencolor('black')
    t.pu()
    t.goto(x,y)
    t.pd()
    t.setheading(n)
    t.fd(300)
    for j in range(6):
        t.rt(90)
        t.fd(50)
        t.rt(90)
        t.pu()
        t.fd(50)
        t.pd()
        t.rt(90)
        t.fd(50)
        t.rt(90)

s(310,310)
s(310,-310)
s(-310,-310)
s(-310,310)

l(0,290,290)
l(1,290,-160)
l(2,-160,-160)
l(3,-160,290)

for i in range(50,200,50):
    r(90,-125+i,-375)
    r(-90,-75+i,375)
    r(0,-375,-75+i)
    r(180,375,-125+i)

f(180,0,75,75)
f(90,1,75,-75)
f(270,3,-75,75)
f(0,2,-75,-75)
done()